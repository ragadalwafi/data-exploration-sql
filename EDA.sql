-- ======================
-- Magnitude Exploration
-- ======================
-- Find total customers by countries
SELECT country, COUNT(customer_key) AS total_customers
FROM gold.dim_customers
GROUP BY country
ORDER BY COUNT(customer_key) DESC
-- Find total customers by gender
SELECT gender, COUNT(customer_key) AS total_customers
FROM gold.dim_customers
GROUP BY gender
ORDER BY COUNT(customer_key) DESC
-- Find total products by category
SELECT category, COUNT(product_name) as total_product
FROM gold.dim_products
GROUP BY category
ORDER BY COUNT(product_name) DESC
-- What is the average costs in each category?
SELECT category ,AVG(cost) AS Avg_cost
FROM gold.dim_products
GROUP BY category
ORDER BY AVG(cost) DESC
-- What is the total revenue generated for each category?
SELECT 
	p.category,
	SUM(f.sales) AS total_revenue
FROM gold.fact_sales f
LEFT JOIN gold.dim_products p
ON f.product_key = p.product_key
GROUP BY p.category
ORDER BY SUM(f.sales) DESC
-- Find total revenue is generated by each customer
SELECT 
c.customer_id,
c.first_name,
c.last_name,
SUM(f.sales) AS total_revenue
FROM gold.fact_sales f
LEFT JOIN gold.dim_customers c
ON f.customer_key = c.customer_key
GROUP BY c.customer_id,
c.first_name,
c.last_name
ORDER BY SUM(f.sales) DESC
-- What is the distribution of sold items across countries?
SELECT 
c.country,
SUM(f.quantity) AS total_sold
FROM gold.fact_sales f
LEFT JOIN gold.dim_customers c
ON f.customer_key = c.customer_key
GROUP BY c.country
ORDER BY SUM(f.quantity) DESC


-- ======================
-- Ranking Analysis
-- ======================
--Which 5 products generate the highest revenue 
SELECT TOP 5 product_name, SUM(sales) AS Total_revenue
FROM gold.fact_sales f
LEFT JOIN gold.dim_products p
ON f.product_key = p.product_key
GROUP BY product_name
ORDER BY SUM(sales) DESC

--Which 5 products generate the lowest revenue 
SELECT TOP 5 product_name, SUM(sales) AS Total_revenue
FROM gold.fact_sales f
LEFT JOIN gold.dim_products p
ON f.product_key = p.product_key
GROUP BY product_name
ORDER BY SUM(sales) 

-- Find the top 10 customers who have generated the highest revenue.
SELECT *
FROM
(SELECT 
c.customer_id,
c.first_name,
c.last_name,
SUM(f.sales) AS total_revenue,
ROW_NUMBER() OVER(ORDER BY SUM(f.sales) DESC) AS top_ten_customers
FROM gold.fact_sales f
LEFT JOIN gold.dim_customers c
ON f.customer_key = c.customer_key
GROUP BY c.customer_id,
c.first_name,
c.last_name)t
