-- the 3 customers with the fewest order placed.
SELECT TOP 3
c.customer_id,
c.first_name,
c.last_name,
COUNT(DISTINCT f.order_number) AS total_orders
FROM gold.fact_sales f
LEFT JOIN gold.dim_customers c
ON f.customer_key = c.customer_key
GROUP BY c.customer_id,
c.first_name,
c.last_name
